    public function addToCart(Request $request)
    {
        $request->validate([
            'variant_id' => 'required|exists:variants,id',
            'quantity'   => 'required|integer|min:1'
        ]);

        $user = auth()->user();
        if (!$user) {
            return redirect()->route('login')->with('error', 'Vui l√≤ng ƒëƒÉng nh·∫≠p ƒë·ªÉ th√™m s·∫£n ph·∫©m v√†o gi·ªè h√†ng!');
        }

        $variant = Variant::findOrFail($request->variant_id);

        $cart = Cart::firstOrCreate(
            ['user_id' => $user->id],
            ['created_at' => now(), 'updated_at' => now()]
        );

        $cartItem = CartItem::where('cart_id', $cart->id)
            ->where('variant_id', $variant->id)
            ->first();

        if ($cartItem) {
            $cartItem->quantity += $request->quantity;
            $cartItem->save();
        } else {
            CartItem::create([
                'cart_id'    => $cart->id,
                'variant_id' => $variant->id,
                'quantity'   => $request->quantity,
            ]);
        }

        // ‚úÖ C·∫≠p nh·∫≠t t·ªïng s·ªë l∆∞·ª£ng v√† t·ªïng ti·ªÅn
        $cart->load('cartItem.variant.product');
        $this->updateCartTotals($cart);

        return redirect()->back()->with('success', 'ƒê√£ th√™m v√†o gi·ªè h√†ng!');
    }


    public function updateQuantity(Request $request, $itemId)
    {
        $request->validate([
            'quantity' => 'required|integer|min:1',
        ]);

        $item = CartItem::findOrFail($itemId);
        $item->quantity = $request->quantity;
        $item->save();

        $cart = $item->cart->load('cartItem.variant.product');
        $this->updateCartTotals($cart); // ‚úÖ c·∫≠p nh·∫≠t l·∫°i cart

        $price = $item->variant->price ?? $item->variant->product->base_price;
        $total = $price * $item->quantity;
        $subtotal = $cart->total_price;

        return response()->json([
            'success' => true,
            'total' => number_format($total, 2),
            'subtotal' => number_format($subtotal, 2),
        ]);
    }


    private function updateCartTotals(Cart $cart)
    {
        $totalQuantity = 0;
        $totalPrice = 0;

        foreach ($cart->cartItem as $item) {
            $price = $item->variant->price ?? $item->variant->product->base_price;
            $totalQuantity += $item->quantity;
            $totalPrice += $price * $item->quantity;
        }

        $cart->update([
            'total_quantity' => $totalQuantity,
            'total_price' => $totalPrice,
        ]);
    }


    public function deleteProduct($itemId)
    {
        $item = CartItem::findOrFail($itemId);

        if (auth()->id() !== $item->cart->user_id) {
            return redirect()->back()->with('error', 'B·∫°n kh√¥ng c√≥ quy·ªÅn xo√° s·∫£n ph·∫©m n√†y.');
        }

        $cart = $item->cart;
        $item->delete();

        // ‚úÖ load l·∫°i cart sau khi xo√° ƒë·ªÉ c·∫≠p nh·∫≠t
        $cart->load('cartItem.variant.product');

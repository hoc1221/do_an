            'variants.attributeValues.attribute' // ƒë·ªÉ l·∫•y ƒë∆∞·ª£c c·∫£ t√™n attribute (VD: "Dung t√≠ch")
        ])->findOrFail($id);

        $categories = Category::all();
        $brands = Brand::all();

        // Tr√≠ch xu·∫•t c√°c gi√° tr·ªã thu·ªôc t√≠nh g·∫Øn v·ªõi s·∫£n ph·∫©m qua variant
        $attributeValues = $product->variants
            ->flatMap(function ($variant) {
                return $variant->attributeValues->map(function ($value) use ($variant) {
                    return [
                        'id' => $value->id,
                        'value' => $value->value,
                        'attribute_name' => $value->attribute->name ?? '',
                        'variant_id' => $variant->id,
                        'price' => $variant->price,
                    ];
                });
            })->unique('id')->values();

        return view('clients.products.productdetail', compact(
            'product',
            'categories',
            'brands',
            'attributeValues'
        ));
    }


